        <h2>Historik</h2>
        <p>
        Shiba är en av de japanska nationalraserna och var ursprungligen avsedd för jakt på småvilt och fågel. 
        När engelska settrar och pointrar importerades från England under perioden l868–l912 blev jakt en sport i Japan. 
        Korsning mellan shiba och engelska hundar blev vanlig och den renrasiga shiban blev under perioden 1912–1926 ytterst sällsynt i ursprungsområdena. 
        Jägare och yrkesfolk engagerade sig från 1928 i uppgiften att bevara den renrasiga shiban och enades om utformningen av rasstandarden l934. 
        År l937 utnämndes shiba till officiell japansk ras.
        </p>
